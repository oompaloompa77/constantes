numeros = [1, 2, 3, 4, 5]
nomes = ["Joaquim", "Maria", "Ana", "José", "Pedro"]

print(numeros)
print(nomes)

print(nomes[0]) #Acessa o primeiro elemento da lista
print(nomes[1]) #Acessa o segundo elemento da lista
print(nomes[2]) #Acessa o terceiro elemento da lista
print(nomes[3]) #Acessa o quarto elemento da lista
print(nomes[4]) #Acessa o quinto elemento da lista

print(nomes[0:3]) #Acessa os três primeiros elementos da lista

print(nomes[-1]) #Acessa o último elemento da lista
print(nomes[-2]) #Acessa o penúltimo elemento da lista'